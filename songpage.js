function SongPage() {
  var URL = window.location.href;
  console.log(URL);
  var url = URL;
  var queryString = url.split("?");

  // console.log(queryString[0]);
  // console.log(queryString[1]);

  const urlParams = new URLSearchParams(queryString[1]);

  for (const entry of urlParams.entries()) {
    console.log(entry[1]);
    var urlkey = entry[1];

    var playlist = Songs.filter((song) => song.title == urlkey)[0];
    //console.log(playlist);

    // const [entry] = urlParams;
    // console.log("search id: "+ entry[1])
    // console.log(bannertitle);

    // Song select
    //  var coverimg = document.getElementById("cover");
    //  coverimg.src = playlist.collection.image;
    //  var audio = document.getElementById("mp3Source");
    //  audio.src = "https://dl.dropbox.com/s/oswkgcw749xc8xs/The-Noisy-Freaks.mp3?dl=1";
    //  console.log(playlist.collection.song);

    var tableNode = document.getElementById("songstable");
    var bannerNode = document.getElementById("banner");

    var bannerdivNode1 = document.createElement("div");
    bannerdivNode1.setAttribute("class", "bannerimgdiv");
    var bannerimgNode = document.createElement("img");
    bannerimgNode.setAttribute("class", "bannerimg");
    bannerimgNode.src = playlist.album;
    bannerdivNode1.append(bannerimgNode);
    var bannerdivNode2 = document.createElement("div");
    bannerdivNode2.setAttribute("class", "bannertitlediv");
    var bannertitle1Node = document.createElement("h6");
    bannertitle1Node.setAttribute("class", "banner-title1");
    bannertitle1Node.innerHTML = "PLAYLIST";
    var bannertitle2Node = document.createElement("h1");
    bannertitle2Node.setAttribute("class", "banner-title2");
    bannertitle2Node.innerHTML = "Daily " + playlist.title;
    var bannertitle3Node = document.createElement("h6");
    bannertitle3Node.setAttribute("class", "banner-title3");
    bannertitle3Node.innerHTML =
      "SpotifyClone." + playlist.collection.length + " songs";
    bannerdivNode2.append(bannertitle1Node, bannertitle2Node, bannertitle3Node);
    bannerNode.append(bannerdivNode1, bannerdivNode2);


    playlist.collection.forEach((mix) => {
      var trNode = document.createElement("tr");
      trNode.setAttribute("class", "table-row");
      trNode.setAttribute("id", "table-row");
      trNode.onclick = () =>
        handleClick(
          mix.id,
          mix.image,
          mix.title,
          mix.album,
          mix.song,
          mix.artist
        );
      var tdNode1 = document.createElement("td");
      tdNode1.setAttribute("class", "song-id");
      tdNode1.innerHTML = mix.id;

      var tdNode2 = document.createElement("td");
      tdNode2.setAttribute("class", "flex-container");

      var divNode1 = document.createElement("div");
      divNode1.setAttribute("class", "img-div");

      var imgNode = document.createElement("img");
      imgNode.setAttribute("class", "song-img");
      imgNode.src = mix.image;

      divNode1.append(imgNode);

      var divNode2 = document.createElement("div");
      divNode2.setAttribute("class", "content-div");

      var divNode21 = document.createElement("div");
      divNode21.setAttribute("class", "song-title");
      divNode21.innerHTML = mix.title;
      var divNode22 = document.createElement("div");
      divNode22.setAttribute("class", "song-artist");
      divNode22.innerHTML = mix.artist;
      divNode2.append(divNode21, divNode22);
      tdNode2.append(divNode1, divNode2);

      var tdNode3 = document.createElement("td");
      tdNode3.setAttribute("class", "album-row");
      tdNode3.innerHTML = mix.album;

      trNode.append(tdNode1, tdNode2, tdNode3);
      tableNode.append(trNode);

      var playerImage = document.getElementById("playerImage");
      playerImage.src = "./coverphotos/2.png";
      var music = document.getElementById("music");
      var songtitle = document.getElementById("songtitle");
      var songalbum = document.getElementById("songalbum");
      var songartist = document.getElementById("songartist");
      songtitle.innerHTML = "Select any song";
      songalbum.innerHTML = "Select any song";
      songartist.innerHTML = "Select any song";

      var music = document.getElementById("music");
      var playButton = document.getElementById("play");
      var pauseButton = document.getElementById("pause");

      function handleClick(id, image, title, album, song, artist) {
        console.log(id);
        playerImage.src = image;
        music.src = song;
        songtitle.innerHTML = title;
        songalbum.innerHTML = album;
        songartist.innerHTML = artist;
        music.play();
        playButton.style.visibility = "hidden";
        pause.style.visibility = "visible";
      }
    });
  }
}
